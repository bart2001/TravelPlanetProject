package travelplanet.model;

public class TravelSeqAndImgAndIdDTO {
	
	private int travel_seq;
	private String img;
	private String id;	

	public TravelSeqAndImgAndIdDTO() {		
	}

	public int getTravel_seq() {
		return travel_seq;
	}

	public void setTravel_seq(int travel_seq) {
		this.travel_seq = travel_seq;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
}
